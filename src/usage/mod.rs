// Notes for converting .upg files to rust enum
// * Trim header
// * Flip and format: ([0-9A-F]*)\t(.*) - $2=0x$1,
// * Fix casing: (\b[a-z]) - \u$1
// * Squash spaces and punctuation: [^\w=,]
// * Unmangle reserved: (.*)(reserved)=(.*) - //0x$1-$3 $2

